package com.gh.greenhouse.domain;

import com.gh.core.utils.Pager;

/**
 * 检测参数
 * @author 吴奇俊
 * 2015-10-18 下午7:08:31
 */
public class Element {

	/**
	 * 元素编号
	 */
	private Integer Element_id;

	/**
	 *元素名称 
	 */
	private String Element_nam;

	/**
	 * 温室编号
	 */
	private Integer Shed_id;

	/**
	 * 检测频率
	 */
	private Integer Element_rate;

	/**
	 * 参数类型
	 * 用户自定义
	 */
	private Integer Element_type;

	/**
	 * 单位
	 */
	private String Element_unit;

	/**
	 * 偏移量
	 */
	private String Element_offset;

	/**
	 * 数据类别
	 */
	private String Data_type;
	/**
	 * 是否被删除
	 */
	private String deleted;

	/**
	 * 检测元素的当前值
	 */
	private Double current_value;
	
	public String getDeleted() {
		return deleted;
	}

	public void setDeleted(String deleted) {
		this.deleted = deleted;
	}


	/**
	 * 检测数据对象，带分页
	 */
	private Pager<Monitor> datas;


	public Integer getElement_id() {
		return Element_id;
	}
	public void setElement_id(Integer element_id) {
		Element_id = element_id;
	}
	public String getElement_nam() {
		return Element_nam;
	}
	public void setElement_nam(String element_nam) {
		Element_nam = element_nam;
	}
	public Integer getShed_id() {
		return Shed_id;
	}
	public void setShed_id(Integer shed_id) {
		Shed_id = shed_id;
	}
	public Integer getElement_rate() {
		return Element_rate;
	}
	public void setElement_rate(Integer element_rate) {
		Element_rate = element_rate;
	}
	public Integer getElement_type() {
		return Element_type;
	}
	public void setElement_type(Integer element_type) {
		Element_type = element_type;
	}
	public String getElement_unit() {
		return Element_unit;
	}
	public void setElement_unit(String element_unit) {
		Element_unit = element_unit;
	}
	public String getElement_offset() {
		return Element_offset;
	}
	public void setElement_offset(String element_offset) {
		Element_offset = element_offset;
	}
	public String getData_type() {
		return Data_type;
	}
	public void setData_type(String data_type) {
		Data_type = data_type;
	}
	public Pager<Monitor> getDatas() {
		return datas;
	}
	public void setDatas(Pager<Monitor> datas) {
		this.datas = datas;
	}

	public Double getCurrent_value() {
		return current_value;
	}

	public void setCurrent_value(Double current_value) {
		this.current_value = current_value;
	}


}
