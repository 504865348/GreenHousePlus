package com.gh.greenhouse.domain;

/**农药
 * @author Danny
 *
 */
public class Pesticides {

	/**
	 * id
	 */
	private Integer Pest_id;
	
	/**
	 * 农药名称
	 */
	private String Pest_name;
	
	/**
	 * 备注
	 */
	private String Remark;
	
	/**
	 * 是否被删除
	 */
	private String deleted;

	public String getDeleted() {
		return deleted;
	}

	public void setDeleted(String deleted) {
		this.deleted = deleted;
	}

	public Integer getPest_id() {
		return Pest_id;
	}

	public void setPest_id(Integer pest_id) {
		Pest_id = pest_id;
	}

	public String getPest_name() {
		return Pest_name;
	}

	public void setPest_name(String pest_name) {
		Pest_name = pest_name;
	}

	public String getRemark() {
		return Remark;
	}

	public void setRemark(String remark) {
		Remark = remark;
	}
}
